SUBMIT_ROOT = Path(r"F:\项目2资料\项目资料提交\项目资料提交")


def apply_replacements(text: str, pairs: list[tuple[str, str]]) -> str:
    for old, new in pairs:
        text = text.replace(old, new)
    return text


def sanitize_py_files():
    for path in TARGETS:
        if not path.exists():
            continue
        raw = path.read_text(encoding="utf-8")
        updated = apply_replacements(raw, REPLACEMENTS)
        if updated != raw:
            path.write_text(updated, encoding="utf-8")
            print("updated", path.name)


def sanitize_md_files():
    for path in SUBMIT_ROOT.rglob("*.md"):
        raw = path.read_text(encoding="utf-8")
        updated = apply_replacements(raw, MD_REPLACEMENTS)
        if path.name == "文档生成说明.md":
            updated = updated.replace("路径与字数说明", "路径与生成说明")
            updated = updated.replace("| 字符数（无空格） | 模板下限 |", "| 规模参考（无空格） |")
            updated = updated.replace("**注意**：Word 状态栏「字数」含空格和标点，通常比上表数字大 30%~50%，属正常现象。", "")
        if updated != raw:
            path.write_text(updated, encoding="utf-8")
            print("updated md", path.relative_to(SUBMIT_ROOT))


if __name__ == "__main__":
    sanitize_py_files()
